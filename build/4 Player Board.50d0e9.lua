function onLoad()
    self.setName('4 Player Board')
    self.setScale({.01,.01,.01})
end

function setup()
    self.setScale({2.95,2.95,2.95})
    self.setPosition({0.5,1.2,8})
    self.setRotation({0,0,0})
    self.setLock(false)
    self.setLock(true)
    self.setLock(false)
    self.setLock(true)
end